while [ -z "$NODES" ]; do
  echo -n "Enter the nodes you wish to connect to [example = $EXAMPLE_NODE]: "
  read NODES
done

if [ "$NODES" != *"--node"* ]; then
  NODES="--node $NODES"
fi

while [ -z "$ACCOUNT_NAME" ]; do
  echo -n "Enter your account name: "
  read ACCOUNT_NAME
